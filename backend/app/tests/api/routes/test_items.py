def test_export_items_csv(
    client: TestClient, superuser_token_headers: dict[str, str], db: Session
) -> None:
    # Create test items
    items = [create_random_item(db) for _ in range(3)]
    
    response = client.get(
        f"{settings.API_V1_STR}/items/export",
        headers=superuser_token_headers,
    )
    
    assert response.status_code == 200
    assert response.headers["Content-Type"] == "text/csv"
    assert response.headers["Content-Disposition"] == "attachment; filename=items.csv"
    
    # Convert response content to string and split into lines
    content = response.content.decode()
    lines = content.strip().split("\n")
    
    # Check CSV structure
    assert lines[0] == "id,title,description,owner_id"  # Header
    assert len(lines) >= 4  # Header + at least 3 items
    
    # Verify our test items are in the CSV
    csv_data = {line.split(",")[0]: line for line in lines[1:]}  # id -> full line mapping
    for item in items:
        item_line = csv_data.get(str(item.id))
        assert item_line is not None
        assert str(item.title) in item_line
        assert str(item.description) in item_line
        assert str(item.owner_id) in item_line


